package ru.aqstream.event.api.dto;

/**
 * Статус события в жизненном цикле.
 *
 * <p>Жизненный цикл события:</p>
 * <pre>
 * DRAFT → PUBLISHED → COMPLETED
 *   ↓         ↓
 * CANCELLED  CANCELLED
 * </pre>
 */
public enum EventStatus {

    /**
     * Черновик — событие создано, но не опубликовано.
     * Можно редактировать все поля.
     */
    DRAFT,

    /**
     * Опубликовано — событие доступно для регистрации.
     * Ограничения на редактирование типов билетов с регистрациями.
     */
    PUBLISHED,

    /**
     * Отменено — событие отменено.
     * Все регистрации должны быть отменены, участники уведомлены.
     */
    CANCELLED,

    /**
     * Завершено — событие прошло.
     * Статистика и отчёты доступны.
     */
    COMPLETED
}
