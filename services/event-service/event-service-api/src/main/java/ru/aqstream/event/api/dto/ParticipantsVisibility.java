package ru.aqstream.event.api.dto;

/**
 * Видимость списка участников события.
 */
public enum ParticipantsVisibility {

    /**
     * Закрытый — список участников виден только организаторам.
     * По умолчанию.
     */
    CLOSED,

    /**
     * Открытый — список участников виден всем зарегистрированным.
     */
    OPEN
}
