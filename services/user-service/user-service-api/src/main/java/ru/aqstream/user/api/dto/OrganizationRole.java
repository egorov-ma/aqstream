package ru.aqstream.user.api.dto;

/**
 * Роли в организации.
 *
 * <p>Определяет уровень доступа члена организации.</p>
 */
public enum OrganizationRole {

    /**
     * Владелец организации.
     * Полный контроль: удаление организации, передача владения, назначение ролей.
     */
    OWNER,

    /**
     * Модератор организации.
     * Управление событиями, членами (кроме владельца), check-in.
     */
    MODERATOR
}
