package ru.aqstream.user.db.entity;

/**
 * Статусы токена авторизации через Telegram бота.
 */
public enum AuthTokenStatus {

    /**
     * Токен создан, ожидает подтверждения в боте.
     */
    PENDING,

    /**
     * Пользователь подтвердил вход в боте.
     */
    CONFIRMED,

    /**
     * Токен истёк (не был подтверждён в течение 5 минут).
     */
    EXPIRED,

    /**
     * Токен использован — JWT токены выданы.
     */
    USED
}
